/**
 * 判断是否开始于指定的字符串
 * @param _src    源字符串
 * @param _target 要查找的字符串
 */
module.exports.startWith = function(_src, _target){
	if (!_src || !_target) return false;
	return (_src.indexOf(_target)==0);  
};